"""
需求大纲 Skill
=============
根据确认后的需求大纲，调用 LLM 生成精简的需求大纲文档。
仅包含：需求背景、需求模块及说明。不展开功能点细节。
"""

from skills.base import BaseSkill


class OutlineDocSkill(BaseSkill):
    """生成精简的需求大纲文档（需求背景 + 模块说明）。"""

    name = "outline_doc"
    display_name = "需求大纲"
    description = "生成精简的需求大纲，仅包含需求背景和模块说明"
    output_type = "markdown"

    system_prompt = """你是一位资深的产品经理，擅长撰写清晰简洁的需求大纲。

请根据用户提供的需求信息，输出一份**精简的需求大纲**。只需要包含以下两个部分，不要展开功能点细节：

# {项目名称} — 需求大纲

## 1. 需求背景

### 1.1 项目背景
描述产品/项目的背景、行业环境和市场机会。

### 1.2 核心问题与痛点
列出当前用户或业务面临的 3-5 个核心痛点。

### 1.3 项目目标
明确本项目要达成的 3-5 个核心目标。

### 1.4 目标用户
描述各类目标用户的角色和基本画像。

## 2. 需求模块及说明

对每个核心模块按以下格式说明：

### 2.x 模块名称
- **模块目标**：该模块要解决什么问题
- **模块范围**：包含哪些能力（概要，不需列功能点）
- **核心价值**：给用户/业务带来什么价值
- **优先级**：高/中/低
- **模块间依赖**：与哪些模块有数据或流程上的依赖关系

要求：
1. 所有内容必须结合项目信息具体展开，禁止空泛的模板文字
2. 每个模块的说明控制在 3-5 句话，保持精简
3. 不要列出具体功能点或用户故事
4. 输出纯 Markdown 格式
"""
